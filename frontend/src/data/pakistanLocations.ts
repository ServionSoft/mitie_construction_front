/** Case-insensitive substring filter. Empty query → no suggestions. */
export function filterPakistanLocations(query: string, limit = 10): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const matched: string[] = [];
  for (const loc of PAKISTAN_LOCATIONS) {
    if (loc.toLowerCase().includes(q)) {
      matched.push(loc);
      if (matched.length >= limit) break;
    }
  }
  return matched;
}
